#include "ft_printf.h"

int	is_flag(int c)
{
	return (c == 'c' || c == 'd'
		|| c == 'i' || c == 's'
		|| c == 'p' || c == 'u'
		|| c == 'x' || c == 'X'
		|| c == '%');
}
